def _compress_jm(
    input_yuv: Path,
    cfg: dict[str, Any],
    codec: str,
    qp: int,
    width: int,
    height: int,
    frames: int,
    fps: int,
    gop_size: int,
    bitstream_dir: Path,
    recon_dir: Path,
    logs_dir: Path,
) -> tuple[Path, Path]:
    jm_cfg = cfg.get("jm", {})
    encoder = Path(jm_cfg.get("encoder_path") or "")
    workdir = Path(jm_cfg.get("workdir") or encoder.parent)
    require_file(encoder, "JM encoder")
    bitstream = bitstream_dir / f"{codec}_qp{qp}.264"
    recon = recon_dir / f"{codec}_qp{qp}.yuv"
    generated_cfg = logs_dir / f"{codec}_qp{qp}.cfg"
    generated_cfg.write_text(
        "\n".join(
            [
                f'InputFile             = "{input_yuv}"',
                f"SourceWidth           = {width}",
                f"SourceHeight          = {height}",
                f"FrameRate             = {fps}",
                f"FramesToBeEncoded     = {frames}",
                f"IntraPeriod           = {gop_size}",
                "FrameSkip             = 0",
                f"LevelIdc              = {jm_cfg.get('level_idc', 51)}",
                f"QPISlice              = {qp}",
                f"QPPSlice              = {qp}",
                f"QPBSlice              = {qp}",
                f'OutputFile            = "{bitstream}"',
                f'ReconFile             = "{recon}"',
            ]
        )
        + "\n",
        encoding="utf-8",
    )
    cmd = [str(encoder), "-f", str(generated_cfg), *map(str, jm_cfg.get("extra_args", []))]
    run_logged(cmd, logs_dir / f"{codec}_encode.log", cwd=workdir)
    return bitstream, recon


def compress_suite(
    input_yuv: Path,
    codec_configs: list[Path],
    qp: int,
    output_dir: Path,
    workers: int,
    input_metadata: Path | None = None,
    hmstyle_csv: bool | None = None,
) -> list[dict[str, Any]]:
    ensure_dir(output_dir)

    def run_one(codec_config: Path) -> dict[str, Any]:
        codec_cfg = load_yaml(codec_config)
        codec_out = output_dir / codec_cfg["codec"]
        return compress_one(input_yuv, codec_config, qp, codec_out, input_metadata, hmstyle_csv)

    results: list[dict[str, Any]] = []
    with ThreadPoolExecutor(max_workers=max(1, workers)) as pool:
        futures = {pool.submit(run_one, path): path for path in codec_configs}
        for future in as_completed(futures):
            results.append(future.result())
    write_json(output_dir / "metadata.json", {"runs": results, "workers": workers})
    return results
